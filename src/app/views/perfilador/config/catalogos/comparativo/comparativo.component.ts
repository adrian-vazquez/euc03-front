import { Component, OnInit } from '@angular/core';
import { ActivatedRoute } from '@angular/router';


@Component({
  selector: 'app-comparativo',
  templateUrl: './comparativo.component.html',
  styleUrls: ['./comparativo.component.scss', '../catalogos.component.scss']
})
export class ComparativoComponent implements OnInit {
  dataSource = ELEMENT_DATA;
  title: string;
  edit: boolean;


  constructor(private route: ActivatedRoute) {
    this.route.queryParams
      .subscribe(params => {
        console.log(params);
        const edit =  params?.edit === 'true';

        if( edit ) {
          this.title = 'COMPARATIVO BRUTO';
        } else {
          this.title = 'RENDIMIENTO BRUTO';
        }
        this.edit = edit;
      }
    );
  }

  ngOnInit(): void {
  }
}

